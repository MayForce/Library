        this.inLine2 = inLine2;
        this.inLine3 = inLine3;
        this.inLine4 = inLine4;
        this.inLine5 = inLine5;
        this.turn1 = turn1;
        this.turn2 = turn2;
        this.turn3 = turn3;
        this.turn4 = turn4;
        this.turn5 = turn5;
